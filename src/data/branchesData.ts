// Helper function to get branches by city
export const getBranchesByCity = (city: string): Branch[] => {
  if (city === 'all') return branchesData;
  return branchesData.filter(branch => branch.city === city);
};

// Helper function to get branch count by city
export const getBranchCountByCity = (): Record<string, number> => {
  const counts: Record<string, number> = {};
  branchesData.forEach(branch => {
    counts[branch.city] = (counts[branch.city] || 0) + 1;
  });
  return counts;
};

